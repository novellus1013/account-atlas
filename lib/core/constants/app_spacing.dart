class AppSpacing {
  const AppSpacing._();

  // Base spacing values
  /// 4px - 아이콘과 텍스트 사이 같이 아주 작은 틈만 띄우는 용도
  static const double xs = 4.0;

  /// 8px - 작은 아이콘 여러개, 카테고리 혹은 태그 같은 칩들의 나열
  static const double sm = 8.0;

  /// 12px - 기본 간격 - 제목과 부제 사이, 리스트 아이템 내부 요소 사이
  static const double md = 12.0;

  /// 16px - 화면 좌우 패딩, 카드 또는 박스 내부 패딩
  static const double basic = 16.0;

  /// 20px - 중간 크기 패딩, 카드 간격
  static const double lg20 = 20.0;

  /// 24px - 큰 섹션 (UI 블록) 사이의 간격
  static const double lg = 24.0;

  /// 32px - 화면 상단 하단에 큰 간격
  static const double xl = 32.0;

  /// 40px - 매우 큰 간격, 특별한 강조가 필요한 경우
  static const double xxl = 40.0;

  // Border radius values
  /// 8px - 작은 요소 (칩, 배지)
  static const double radiusSm = 8.0;

  /// 12px - 기본 버튼, 입력 필드
  static const double radiusMd = 12.0;

  /// 16px - 카드, 컨테이너
  static const double radiusLg = 16.0;

  /// 20px - 큰 카드, 모달
  static const double radiusXl = 20.0;

  // Icon sizes
  /// 16px - 작은 아이콘 (인라인, 버튼 내부)
  static const double iconSm = 16.0;

  /// 20px - 기본 아이콘
  static const double iconMd = 20.0;

  /// 24px - 중간 아이콘
  static const double iconLg = 24.0;

  /// 40px - 큰 아이콘 (리스트 아이템)
  static const double iconXl = 40.0;

  /// 48px - 프로필/서비스 아이콘
  static const double iconXxl = 48.0;

  /// 56px - 큰 서비스 아이콘
  static const double icon56 = 56.0;

  /// 64px - 상태 표시 아이콘
  static const double icon64 = 64.0;

  /// 80px - 상세 화면 메인 아이콘
  static const double icon80 = 80.0;
}
